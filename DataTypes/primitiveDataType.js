function exampleFunction() {
    let exampleVar = "Hello";//string
    console.log(typeof (exampleVar));
    // Output:string
    
    let age = 25;//int
    console.log(typeof (age));
    // Output:number
    
    let pi = 3.14;//float
    console.log(typeof (pi));
    // Output:number
    
    let isStudent = true;//bool
    console.log(typeof (isStudent));
    // Output:boolean
    
    let isUndefined;//undefined
    console.log(typeof (isUndefined));
    // Output:undefined
    
    let isNull = null;//NULL
    console.log(typeof (isNull));
    // Output:object

} exampleFunction();